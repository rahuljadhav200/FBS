#----Assignment Operator-----

#1. =  (equals to)
x=10
print(x)

#2. += (addition equal to)
x+=3  #x= x+3
print(x)

#3. -= (subs equal to)
x-=5
print(x)

#4. *= (Mul equlas to)
x*=7
print(x)

#5. /= (div equals to)
x/=11
print(x)

#6. //= (floor div equals to)
x//=10
print(x)

#7. %= (modulus equal to)
x%=5
print(x)

#8. **= (expo equals to)
x**=15
print(x)
